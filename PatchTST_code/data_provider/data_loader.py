FEAT_ID = {
    "time_right_xyz": slice(0, 4),
    "right_euler": slice(4, 7),
    "right_trigger": slice(7, 8),
    "headset_xyz": slice(8, 11),
    "headset_euler": slice(11, 14),
    "headset_trigger": slice(14, 15),
    "left_xyz": slice(15, 18),
    "left_euler": slice(18, 21),
    "left_trigger": slice(21, 22)
}

def euler2quat(euler_angles):
    quat_list = []
    for i in range(euler_angles.shape[0]):
        quat_vector = quat.axangle2quat(np.array([0, 0, 1]), np.deg2rad(euler_angles[i][2]))
        quat_vector = quat.qmult(quat_vector, quat.axangle2quat(np.array([0, 1, 0]), np.deg2rad(euler_angles[i][1])))
        quat_vector = quat.qmult(quat_vector, quat.axangle2quat(np.array([1, 0, 0]), np.deg2rad(euler_angles[i][0])))
        quat_list.append(quat_vector)
    quat_list = np.vstack(quat_list)
    return quat_list


def sliding_window(genuine, ws=50, stride=1):
    sessions, timestamps, features = genuine.shape
    
    all_genuine_sub_sess = []
    for s in range(sessions):
        start_pos = 0
        end_pos = ws
        genuine_sub_sess = []
        while(end_pos < timestamps):
            genuine_sub_sess.append(genuine[s, start_pos:end_pos, :])
            start_pos += stride
            end_pos += stride
        genuine_sub_sess = np.array(genuine_sub_sess)
        
        all_genuine_sub_sess.append(genuine_sub_sess)

    all_genuine_sub_sess = np.array(all_genuine_sub_sess)
    all_genuine_sub_sess = np.reshape(all_genuine_sub_sess, [-1, ws, features])

    return all_genuine_sub_sess


class Forecast_Only_Dataset(Dataset):
    def __init__(
            self,
            root,
            use_data="Data_1",
            split="train",
            split_rate=0.8,
            forecasting_sizes=[25, 15, 25],
        ):
        assert use_data == "Data_1"
        assert split in ["train", "test"]

        data = np.load(os.path.join(root, f"{use_data}.npz"), allow_pickle=True)

        self.num_timestamps = data["Quest1"].item()["user_00"].shape[1]
        self.seq_len, self.label_len, self.pred_len = forecasting_sizes

        all_slided_genuine = []
        for _, value in data.items():

            for user in value.item().keys():
                raw_data = value.item()[user]
                data_shape = raw_data.shape
                time_feature = [i+1 for i in range(self.num_timestamps)]
                time_feature = np.array(time_feature)

                raw_data_with_time = np.zeros([
                    data_shape[0],
                    data_shape[1], 
                    data_shape[2]+1, 
                ])
                raw_data_with_time[:, :, 1:] = raw_data
                raw_data_with_time[:, :, 0] = time_feature

                ws = self.seq_len + self.pred_len
                slided_genuine = sliding_window(raw_data_with_time, ws, 1)

                all_slided_genuine.append(slided_genuine)

        all_slided_genuine = np.array(all_slided_genuine)
        all_slided_genuine = np.reshape(all_slided_genuine, [-1, ws, all_slided_genuine.shape[-1]])

        total_num_of_data = all_slided_genuine.shape[0]
        train_test_bound = int(split_rate * total_num_of_data)
        if split == "train":
            self.data2use = all_slided_genuine[0:train_test_bound, :, :]
        else:
            self.data2use = all_slided_genuine[train_test_bound:, :, :]
        

    def __getitem__(self, idx):
        slided_genuine = self.data2use[idx]

        time_right_traj = slided_genuine[:, FEAT_ID["time_right_xyz"]]
        right_quat = euler2quat(slided_genuine[:, FEAT_ID["right_euler"]])
        right_trigger = slided_genuine[:, FEAT_ID["right_trigger"]].reshape(-1, 1)

        headset_traj = slided_genuine[:, FEAT_ID["headset_xyz"]]
        headset_quat = euler2quat(slided_genuine[:, FEAT_ID["headset_euler"]])

        left_traj = slided_genuine[:, FEAT_ID["left_xyz"]]
        left_quat = euler2quat(slided_genuine[:, FEAT_ID["left_euler"]])
        left_trigger = slided_genuine[:, FEAT_ID["left_trigger"]].reshape(-1, 1)

        data = np.hstack((
            time_right_traj, right_quat, right_trigger,
            headset_traj, headset_quat,
            left_traj, left_quat, left_trigger
        ))

        encoder_input = data[:self.seq_len, 1:]
        encoder_time_feat = data[:self.seq_len, 0] / self.num_timestamps - 0.5

        decoder_input = data[self.seq_len - self.label_len:, 1:]
        decoder_time_feat = data[self.seq_len-self.label_len:, 0]/self.num_timestamps - 0.5
        return (
            encoder_input, np.expand_dims(encoder_time_feat, 1),
            decoder_input, np.expand_dims(decoder_time_feat, 1),
            data
        )


    def __len__(self):
        return (self.data2use.shape[0])